public class Calcu {
	int add(int x, int y) {
//		int r  = a + b;
//				return r;
		return x + y;
	}
	int add(int a, int b, int c) {
		return a + b +c;
	}
	double add(double a, double b) {
		return a + b;
	}
	double add(double a, double b, double c) {
		return a + b + c;
	}
	int subtract(int a, int b) {
		return a - b;
	}

}
